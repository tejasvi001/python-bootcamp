# Primitive data types in Python.
#
# Strings   - array of characters
# Integers  - integers whole numbers and negative of whole numbers
# Float     - decimals
# Booleans  - true or false

#indexing in strings
print("hello"[4])
# negative indexing starts from end
print("hello"[-1])
# use + to concatenate strings
print("hello "+"world")

#integers
print("123")
#large integers can have underscore between them
print("123_456_789")

#Floating point numbers - pi
print("3.14")

#boolean values
print(True)
print(False)

